#include "hw04.hpp"

void multiplyMatrices(int** A, int** B, int rowsA, int colsA, int colsB, int** result) {
    for (int i = 0; i < rowsA; i++) {
        for (int j = 0; j < colsB; j++) {
            result[i][j] = 0;
            for (int k = 0; k < colsA; k++) {
                result[i][j] += A[i][k] * B[k][j];
            }
        }
    }
}
